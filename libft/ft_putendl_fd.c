#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	unsigned int	id;

	id = 0;
	if (s)
	{
		while (s[id] != '\0')
		{
			write(fd, &s[id], 1);
			id++;
		}
		write(fd, "\n", 1);
	}
}

#include "../includes/ps.h"

void	check_str(char *arg)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = ft_strlen(arg);
	while (i < len)
	{
		if ((arg[i] > '9' || arg[i] < '0') && arg[i] != ' '
				&& !(arg[i] == '-' && arg[i + 1] != '-'))
		{
			write(2, "Error\n", 6);
			exit(0);
		}
		i++;
	}
}
